#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (str1[i] != '\0')
		i++;
	while (str2[c] != '\0')
	{
		str1[i] = str2[c];
		i++;
		c++;
	}
	str1[i] = '\0';
	return (str1);
}
